namespace NeatInput.Windows.Processing.Mouse.Enums
{
    public enum MouseKeys
    {
        /// <summary>
        /// Left mouse button.
        /// </summary>
        LBUTTON = 0x01,
        /// <summary>
        /// Right mouse button.
        /// </summary>
        RBUTTON = 0x02,
        /// /// <summary>
        /// Middle mouse button (three-button mouse).
        /// </summary>
        MBUTTON = 0x04,
        /// <summary>
        /// Windows 2000/XP: X1 mouse button.
        /// </summary>
        XBUTTON1 = 0x05,
        /// <summary>
        /// Windows 2000/XP: X2 mouse button.
        /// </summary>
        XBUTTON2 = 0x06,
        WHEEL,
        SCANDEVICE
    }
}
